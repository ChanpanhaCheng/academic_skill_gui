    private void loadAttendanceData() {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data

        try {
            String query = "SELECT s.stu_id, CONCAT(u.first_name, ' ', u.last_name) as fullname, " +
                         "s.session_no, s.status FROM studentattendance s " +
                         "JOIN User u ON u.id = s.stu_id";
            
            PreparedStatement pst = connection.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String studentId = rs.getString("stu_id");
                String fullName = rs.getString("fullname");
                String sessionNo = rs.getString("session_no");
                String status = rs.getString("status");

                model.addRow(new Object[]{studentId, fullName, sessionNo, status});
            }
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error loading attendance data: " + e.getMessage(),
                "Database Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }
