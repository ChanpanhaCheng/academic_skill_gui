    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // Update button action - using original variable names
        String studentId = jTextField1.getText();
         String status = jComboBox1.getSelectedItem().toString();
        String sessionNo = jTextField3.getText();

        try {
            String updateQuery = "UPDATE studentattendance SET status = ? " +
                               "WHERE stu_id = ? AND session_no = ?";
            
            PreparedStatement pst = connection.prepareStatement(updateQuery);
            pst.setString(1, status);
            pst.setString(2, studentId);
            pst.setString(3, sessionNo);

            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Attendance updated successfully",
                    "Success",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
                loadAttendanceData(); // Refresh the table
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "No matching record found. Please check Student ID and Session No.",
                    "Warning",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error updating attendance: " + e.getMessage(),
                "Database Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    
    }                                        

